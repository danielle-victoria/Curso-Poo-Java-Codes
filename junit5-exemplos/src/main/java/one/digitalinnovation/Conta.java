package one.digitalinnovation;

public class Conta {

    private String numeroConta;
    private int saldo;

    public Conta(String numeroConta, int saldo){
        this.numeroConta = numeroConta;
        this.saldo = saldo;
    }

    public String getNumeroConta(){
        return numeroConta;
    }

    public int getSalario(){
        return saldo;
    }

    

    
}
